@app.route("/", methods=["GET", "POST"])
def index():
    if "user" not in session:
        return redirect(url_for("login"))

    if request.method == "POST":
        file = request.files["file"]
        path = os.path.join(UPLOAD_FOLDER, file.filename)
        file.save(path)
        return redirect(url_for("analyze", filename=file.filename))

    return render_template("index.html", user=session["user"])

@app.route("/analyze/<filename>")
def analyze(filename):
    if "user" not in session:
        return redirect(url_for("login"))

    df = pd.read_csv(os.path.join(UPLOAD_FOLDER, filename))
    texts = df["comment_text"].astype(str).tolist()

    sentiments, confidences = predict_sentiment(texts)
    df["sentiment"] = sentiments
    df["confidence"] = confidences

    # Flag uncertain predictions
    df["uncertain"] = df["confidence"] < 0.6

    counts = df["sentiment"].value_counts().to_dict()
    total = len(df)
    uncertain_count = int(df["uncertain"].sum())

    percentages = {
        "positive": round(counts.get("positive",0)*100/total,2),
        "neutral": round(counts.get("neutral",0)*100/total,2),
        "negative": round(counts.get("negative",0)*100/total,2)
    }

    generate_wordcloud(texts)

    summary_pos = summarize(df[df["sentiment"]=="positive"]["comment_text"].tolist())
    summary_neu = summarize(df[df["sentiment"]=="neutral"]["comment_text"].tolist())
    summary_neg = summarize(df[df["sentiment"]=="negative"]["comment_text"].tolist())

    # Save history
    hist = pd.read_excel(HISTORY_DB)
    hist.loc[len(hist)] = [
        session["user"], filename, datetime.now().strftime("%Y-%m-%d %H:%M"),
        percentages["positive"], percentages["neutral"], percentages["negative"],
        uncertain_count
    ]
    hist.to_excel(HISTORY_DB, index=False)
    df.to_csv(os.path.join("static", "last_analysis.csv"), index=False)


    # Generate PDF
    pdf_path = generate_pdf_report(
        session["user"],
        percentages,
        {"pos": summary_pos, "neu": summary_neu, "neg": summary_neg}
    )

    return render_template(
        "dashboard.html",
        total=total,
        percentages=percentages,
        summary_pos=summary_pos,
        summary_neu=summary_neu,
        summary_neg=summary_neg,
        user=session["user"],
        uncertain=int(df["uncertain"].sum()),
        pdf_ready=True
    )


