def hash_password(pw):
    return hashlib.sha256(pw.encode()).hexdigest()

def generate_pdf_report(user, stats, summaries):
    path = os.path.join(STATIC_FOLDER, "report.pdf")

    doc = SimpleDocTemplate(path, pagesize=A4)
    styles = getSampleStyleSheet()
    story = []

    story.append(Paragraph("<b>MCA Policy Feedback Analysis Report</b>", styles["Title"]))
    story.append(Spacer(1, 12))

    story.append(Paragraph(f"Generated by: {user}", styles["Normal"]))
    story.append(Paragraph(f"Date: {datetime.now().strftime('%Y-%m-%d %H:%M')}", styles["Normal"]))
    story.append(Spacer(1, 12))

    story.append(Paragraph("<b>Summary Statistics</b>", styles["Heading2"]))
    story.append(Paragraph(f"Positive: {stats['positive']}%", styles["Normal"]))
    story.append(Paragraph(f"Neutral: {stats['neutral']}%", styles["Normal"]))
    story.append(Paragraph(f"Negative: {stats['negative']}%", styles["Normal"]))
    story.append(Spacer(1, 12))

    story.append(Paragraph("<b>Summaries</b>", styles["Heading2"]))
    story.append(Paragraph("<b>Positive Feedback</b>", styles["Normal"]))
    story.append(Paragraph(summaries["pos"], styles["BodyText"]))
    story.append(Spacer(1, 8))

    story.append(Paragraph("<b>Neutral Feedback</b>", styles["Normal"]))
    story.append(Paragraph(summaries["neu"], styles["BodyText"]))
    story.append(Spacer(1, 8))

    story.append(Paragraph("<b>Negative Feedback</b>", styles["Normal"]))
    story.append(Paragraph(summaries["neg"], styles["BodyText"]))
    story.append(Spacer(1, 12))

    # Wordcloud
    wc_path = os.path.join(STATIC_FOLDER, "wordcloud.png")
    if os.path.exists(wc_path):
        story.append(Paragraph("<b>Word Cloud</b>", styles["Heading2"]))
        story.append(Image(wc_path, width=400, height=200))

    doc.build(story)
    return path


def predict_sentiment(texts):
    labels = []
    confidences = []

    for i in range(0, len(texts), 32):
        batch = texts[i:i+32]
        inputs = tokenizer(batch, padding=True, truncation=True, max_length=256, return_tensors="pt").to(device)

        with torch.no_grad():
            outputs = model(**inputs)
            probs = F.softmax(outputs.logits, dim=1)

        preds = torch.argmax(probs, dim=1)

        for p, prob in zip(preds, probs):
            labels.append(LABEL_MAP[int(p)])
            confidences.append(float(prob[int(p)]))

    return labels, confidences

def generate_wordcloud(texts):
    wc = WordCloud(width=1200, height=600, background_color="white")
    wc.generate(" ".join(texts))
    wc.to_file(os.path.join(STATIC_FOLDER, "wordcloud.png"))

def summarize(texts):
    if len(texts) == 0:
        return "No comments in this category."
    joined = " ".join(texts[:20])[:3500]
    out = summarizer(joined, max_length=180, min_length=80, do_sample=False)
    return out[0]["summary_text"]

